/*---------------Delay function----------------*/
void delay_us (uint16_t us)
 { while(us--);
 }

void delay_ms (uint16_t ms)
 { uint16_t i,j;
   for(i=0;i<ms;i++)
   for(j=0;j<3000;j++)
      ;
 }

/*------------Write data or command to LCD--------------*/
void wr_od (uint8_t dat,uint8_t comm)       //write one byte data and one byte command
 { wr_data(dat);
   wr_comm(comm);
 }

void wr_td (uint8_t datl,uint8_t dath,uint8_t comm)  //write two bytes data and one byte command
 { wr_data(datl);
   wr_data(dath);
   wr_comm(comm);
 }

void wr_xd (uint16_t dat,uint8_t comm)       //write a 16 bits data and one byte command
 { wr_data(dat);
   wr_data(dat>>8);
   wr_comm(comm);
 }

void wr_auto (uint8_t dat)               //Auto write
 { chk_busy (1);
   IO0CLR=cd;
   IO0SET=rd;
    IO0PIN=( IO0PIN&0XFFFFFF00|dat);
   IO0CLR=wr;
   IO0SET=wr;
 }

void wr_comm (uint8_t comm)       //write command
 { chk_busy (0);
   IO0SET=cd;
   IO0SET=rd;
    IO0PIN=( IO0PIN&0XFFFFFF00|comm);
   IO0CLR=wr;
   IO0SET=wr;
 }

void wr_data (uint8_t dat)       //write data
 { chk_busy (0);
   IO0CLR=cd;
   IO0SET=rd;
    IO0PIN=( IO0PIN&0XFFFFFF00|dat);
   IO0CLR=wr;
   IO0SET=wr;
 }

void chk_busy (uint8_t autowr)    //test busy flag
 { IO0SET=0xff;
   IO0SET=cd;
   IO0SET=wr;
    IO0DIR &= 0xFFFFFF00;
   IO0CLR=rd;
   if(autowr)
    { while(( IO0PIN&bf3)==0);
    }
   else
    { while(( IO0PIN&bf0)==0||( IO0PIN&bf1)==0);
    }
   IO0SET=rd;
    IO0DIR |= 0x000000FF;
 }

/*--------------Clear RAM------------------*/
void lcd_clr_ram (void)
 { uint8_t i,j;
   wr_xd(addr_w,0x24);
   wr_comm(0xb0);
   for(j=0;j<144;j++)
    { for(i=0;i<width;i++)
         wr_auto(0x00);
    }
   wr_comm(0xb2);
 }

/*--------------Display matrix------------------*/
void lcd_disp_matrix (uint8_t data1,uint8_t data2)
 { uint8_t i,j;
   wr_xd(addr_t,0x24);
   wr_comm(0xb0);
   for(j=0;j<64;j++)
    { for(i=0;i<width;i++)
         wr_auto(data1);
      for(i=0;i<width;i++)
         wr_auto(data2);
    }
   wr_comm(0xb2);
 }

/*--------------Draw a (8*l)*yl picture at addr-------------*/
void lcd_disp_imgage (uint8_t addr,uint8_t xl,uint8_t yl,uint8_t const *img)
 { uint8_t i,j;
   for(j=0;j<yl;j++)
    { for(i=0;i<xl;i++)
       { wr_xd(addr+0X100+j*width+i,0x24);
         wr_od(img[j*xl+i],0xc0);
       }
    }
 }

/*----------Draw a Chinese character at addr----------*/
void lcd_disp_char_chinese (uint16_t addr,uint8_t xl,uint8_t yl,uint8_t row_xl,uint8_t row_yl,uint8_t const  *chn)
 { uint8_t i,j,k,m;
   for(m=0;m<row_yl;m++)
    { for(k=0;k<row_xl;k++)
       { for(j=0;j<yl;j++)
          { for(i=0;i<xl;i++)
             { wr_xd(addr+m*yl*width+k*xl+j*width+i,0x24);
               wr_od(chn[(m*row_xl*xl*yl)+(k*xl*yl)+(j*xl)+i],0xc0);
             }
          }
       }
    }
 }

/*--------------Draw string------------------*/
void disp_eng (uint8_t const *eng)
 { uint8_t i,j;
   wr_xd(addr_w,0x24);
   wr_comm(0xb0);
   for(j=0;j<10;j++)
    { for(i=0;i<width;i++)
         wr_auto(eng[j*width+i]);
    }
   wr_comm(0xb2);
 }

/*----------Draw a Chinese character at position (x,y)----------*/
void lcd_disp_msg_chinese(uint32_t x,uint32_t y,uint8_t  *str)
 { uint8_t i,j,n,slen,m=0;
   uint32_t tempx,tempy;
   tempx=x;
   tempy=y;
   slen = strlen((char const*)str);
  
	while(m<slen-1)
	 { uint8_t c1,c2;
		 
		 c1=str[m+0];c2=str[m+1];
      for(n=0;n<sizeof(gb_16)/sizeof(gb_16[0]);n++)
       { if(c1 == gb_16[n].Index[0]&&c2 == gb_16[n].Index[1])
         break;
       }
      y=tempy;
      x=tempx;   
      for(i=0;i<16;i++)
       { for (j=0;j<2;j++)
          { wr_xd((0x0100+256*y+16*i+x+j),0x24);
            wr_od(gb_16[n].Msk[i*2+j],0xc0);    
          }
       }
      m=m+2;//one Chinese charecter has two bytes, LCDSIZE/8
      tempx += 2;
    
      if(tempx>15)
       { tempx = 0;
         tempy += 1;
       }
    }
 }

/*----------Draw a character at position (x,y)----------*/
void lcd_disp_msg(uint32_t x,uint32_t y,uint8_t *str)
 { 
   wr_xd((addr_w+16*y+x),0x24);
   wr_comm(0xb0);
   while(*str!='\0')
    { char c;
			c = (*str);
      wr_auto(c-32);
      str++;
    }
   wr_comm(0xb2);
 }
 
